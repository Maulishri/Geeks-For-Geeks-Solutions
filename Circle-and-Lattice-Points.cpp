#include<iostream>
using namespace std;
int main()
 {
     int n;
     cin>>n;
     while(n--)
     {
         int l;
         cin>>l;
         int count=0;
         for(int i=-l; i<=l; i++)
         {
             for(int j=-l; j<=l; j++)
             {
                if((i*i + j*j) == l*l)
                    count++;
                
             }
         }
         cout<<count<<endl;
         
     }
	//code
	return 0;
}
